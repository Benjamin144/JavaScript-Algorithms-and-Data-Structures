/*
Like the += operator, -= subtracts a number from a variable.
myVar = myVar - 5;
will subtract 5 from myVar. This can be rewritten as:

myVar -= 5; */

let a = 11;
let b = 9;
let c = 3;

// this converts the assignment for a,b & c to usw -= operator by shorthand 

a -= 6; 
b -= 15; 
c -= 1; 

console.log(a,b,c)


