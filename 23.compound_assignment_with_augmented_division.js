/* Compound Assignment With Augmented Division
The /= operator divides a variable by another number.

myVar = myVar / 5;
Will divide myVar by 5. This can be rewritten as:

myVar /= 5; */

let a = 48;
let b = 108;
let c = 33;

a /= 12; [4] // denoting the answer to this value function
b /= 4; [27]
c /= 11; [3]

